(* ScalarGluonVertex *)
(* ::Text:: *)
(*ScalarGluonVertex[{p}, {q}, {\[Mu], a}] or ScalarGluonVertex[p,  q,  \[Mu], a] yields the scalar-scalar-gluon vertex, where $\text{p}$ and $\text{q}$ are incoming momenta.   ScalarGluonVertex[{\[Mu], a}, {\[Nu], b}] yields the scalar-scalar-gluon-gluon vertex, where $\text{p}$ and $\text{q}$ are incoming momenta. syntax.The dimension and the name of the coupling constant are determined by the options Dimension and CouplingConstant..*)


(* ::Subsection:: *)
(* Examples *)
ScalarGluonVertex[{p}, {q}, {\[Mu], a}]
