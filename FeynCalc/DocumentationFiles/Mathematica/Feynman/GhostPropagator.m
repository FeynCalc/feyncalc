(*GhostPropagator*)


(* ::Text:: *)
(*`GhostPropagator[p, a, b]` gives the ghost propagator where `a` and `b` are the color indices.*)


(* ::Text:: *)
(*`GhostPropagator[p]` omits the $\delta _{ab}$.*)


(* ::Text:: *)
(*`GHP` can be used as an abbreviation of `GhostPropagator`.*)


