(*ConvoluteTable*)


(* ::Text:: *)
(*`ConvoluteTable[f, g, x]` yields the convolution of `f` and `g`. `ConvoluteTable` is called by `Convolute`.*)


(* ::Subsection:: *)
(*See also*)


(* ::Text:: *)
(*[PlusDistribution](PlusDistribution), [Convolute](Convolute).*)


(* ::Subsection:: *)
(*Examples*)


ConvoluteTable[1,1,x]


ConvoluteTable[x,x]


ConvoluteTable[1,x,x]
