(* LorentzToCartesian *)
(* ::Text:: *)
(*LorentzToCartesian[exp]  rewrites Lorentz tensors in form of Cartesian tensors (when possible). Using options one can specify which types of tensors should be converted..*)


(* ::Subsection:: *)
(* Examples *)
SPD[p,q]

%//LorentzToCartesian

LC[mu,nu][p,q]

%//LorentzToCartesian
