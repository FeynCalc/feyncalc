(*FCFilePatch*)


(* ::Text:: *)
(*`FCFilePatch[input,output, rp]` replaces the patterns given by `rp` in the file `input` and writes the modified version to `output`. Here `rp` should be a list of the form `{string -> string, ...}`.*)


