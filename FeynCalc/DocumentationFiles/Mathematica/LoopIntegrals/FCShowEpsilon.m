(* FCShowEpsilon *)
(* ::Text:: *)
(*FCShowEpsilon[expr] substitutes $\text{SMP}[\text{Delta}]$ with $1/\text{Epsilon} - \text{EulerGamma} + \text{Log}[4\text{Pi}]$.*)


(* ::Subsection:: *)
(* Examples *)


SMP["Delta"]
FCShowEpsilon[%]


SMP["Delta_UV"]
FCShowEpsilon[%]


SMP["Delta_IR"]
FCShowEpsilon[%]
