(*Power2*)


(* ::Text:: *)
(*`Power2[x, y]` represents `x^y`.  Sometimes `Power2` is more useful than the Mathematica `Power`. `Power2[-a,b]` simplifies to `(-1)^b Power2[a,b]` (if no `Epsilon` is in `b` ...).*)


