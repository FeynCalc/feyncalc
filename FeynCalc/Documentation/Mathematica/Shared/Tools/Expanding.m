(*Expanding*)


(* ::Text:: *)
(*Expanding is an option for `Contract`, `DiracSimplify`, `DotSimplify`, `SUNSimplify` etc. *)


(* ::Text:: *)
(*As option for `Contract` it specifies whether expansion w.r.t. `LorentzIndex` is done _before_ contraction. If set to `False` in `DiracSimplify` or `SUNSimplify`, only a limited set of simplifications (multiplicative linearity etc.) is performed.*)


